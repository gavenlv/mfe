/**
 * 文件级注释：main.jsx
 * 所属微前端：auth（认证微前端）
 * 核心职责：整个 auth 微前端的"异步边界入口"。
 * 关键概念（为什么 main.jsx 里只有一行动态 import？）：
 *   模块联邦要求：remote 暴露的模块在被宿主加载时，必须以异步方式初始化，
 *   这样 shared（共享依赖）才能在"宿主已加载依赖"与"微前端独立运行"两种场景下
 *   都被正确解析与共享。如果直接在入口同步 import React 再渲染，
 *   微前端独立运行时可以工作，但被宿主加载时会先于 shared 协商执行，
 *   导致 React 被加载两份（破坏单例、路由 context 失效）。
 *   解决办法：main.jsx 只做一件事——动态 import('./bootstrap.jsx')，
 *   把真正的渲染逻辑推迟到下一个事件循环（异步边界），等 shared 依赖协商完成后再执行。
 *   因此这个看似多余的"中间层"是微前端架构的关键。
 */
import('./bootstrap.jsx'); // 动态导入：异步加载真正的启动逻辑 bootstrap.jsx，制造异步边界
